package com.demo;

public class StringDemo2 {

	public static void main(String[] args) {
		String s = "Mitul";
		s.concat(" Mistry");
		System.out.println("Literal string is :" + s);

		String s1 = new String("Prithwish");
		s1.concat(" Shah");
		System.out.println("Object string is :" + s1);

		// String class: immutable

	}
}
